enum ParseError {
    UnknownChar,
}

fn pretty_print_map(input: &Vec<Vec<u8>>) {
    input.iter().for_each(|row| println!("{:?}", row));
}

fn parse_input(input: &str) -> Result<Vec<Vec<u8>>, ParseError> {
    let mut inner: Vec<u8> = Vec::new();
    let mut outer: Vec<Vec<u8>> = Vec::new();
    input.chars().for_each(|x| match x {
        '.' => inner.push(0),
        '@' => inner.push(1),
        '\n' => {
            outer.push(inner.clone());
            inner.clear();
        }
        _ => inner.push(255),
    });

    outer.push(inner.clone());

    if inner.contains(&255) {
        return Err(ParseError::UnknownChar);
    }

    Ok(outer)
}

fn append_border(input: &Vec<Vec<u8>>) -> Vec<Vec<u8>> {
    let mut outer: Vec<Vec<u8>> = Vec::new();

    outer.push(vec![0; input[0].len() + 2]);
    for row in input.iter() {
        let mut new_row = Vec::with_capacity(input[0].len() + 2);
        new_row.push(0);
        new_row.extend_from_slice(row);
        new_row.push(0);
        outer.push(new_row);
    }
    outer.push(vec![0; input[0].len() + 2]);

    outer
}

fn check_access(input: &Vec<Vec<u8>>) -> Vec<Vec<u8>> {
    let mut inner: Vec<u8> = Vec::new();
    let mut outer: Vec<Vec<u8>> = Vec::new();

    for i in 1..input.len() - 1 {
        //println!("row: {}", i);
        for j in 1..input[i].len() - 1 {
            if input[i][j] == 0 {
                inner.push(0);
                continue;
            }

            //println!("char: {}", j);
            let mut counter = 0;
            for k in 0..3 {
                for l in 0..3 {
                    //println!("checking: {}:{}", i - 1 + k, j - 1 + l);
                    if i - 1 + k == i && j - 1 + l == j {
                        continue;
                    }

                    if input[i - 1 + k][j - 1 + l] == 1 {
                        counter += 1;
                    }
                }
            }

            if counter >= 4 {
                inner.push(1);
            } else {
                inner.push(2);
            }
        }

        outer.push(inner.clone());
        inner.clear();
    }

    outer
}

fn count_accessible(input: &Vec<Vec<u8>>) -> i32 {
    let mut c = 0;
    input.iter().for_each(|inner| {
        inner.iter().for_each(|v| match v {
            2 => c += 1,
            _ => c += 0,
        });
    });

    c
}

fn remove_accessible(input: &Vec<Vec<u8>>) -> Vec<Vec<u8>> {
    let mut inner: Vec<u8> = Vec::new();
    let mut outer: Vec<Vec<u8>> = Vec::new();
    input.iter().for_each(|row| {
        row.iter().for_each(|v| match v {
            2 => inner.push(0),
            1 => inner.push(1),
            _ => inner.push(0),
        });

        outer.push(inner.clone());
        inner.clear();
    });

    outer
}

